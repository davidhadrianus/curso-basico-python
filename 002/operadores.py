# Operadores

# aritmeticos: +, -, *, /, // (divisao inteira), % (resto), ** (potencia)
# relacionais: ==, !=, >, <, >=, <=
# logicos: and, or, not

a = 20
b = 12

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

print(a and b)
print(a or b)
